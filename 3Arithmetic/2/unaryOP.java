public class unaryOP{
    public static void main(String[]args){
        int a=10;
        int b=--a;
        System.out.println(a);
        System.out.println(b);


        int ab=10;
        int ba=ab++;
        System.out.println(ab);
        System.out.println(ba);

      int x = 2, y = 5;
      int exp1 = (x * y / x);
      int exp2 = (x * (y / x));
      System.out.print(exp1 + " , ");
      System.out.print(exp2);

    }
}